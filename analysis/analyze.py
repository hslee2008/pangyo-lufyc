def calculate_person_statistics(data, person_id):
    # Initialize values to store results for the specific person
    total_accumulation = 0
    rating_count = 0
    rating_distribution = {}

    # Loop through each category and its ratings
    for category, ratings in data.items():
        # Check if the person exists in the current category
        if person_id in ratings:
            rating = ratings[person_id]
            # Accumulate total rating for the person
            total_accumulation += rating

            # Increment the count of ratings
            rating_count += 1

            # Update the rating distribution
            if rating not in rating_distribution:
                rating_distribution[rating] = 0
            rating_distribution[rating] += 1

    return {
        "total_accumulation": total_accumulation,
        "rating_count": rating_count,
        "rating_distribution": rating_distribution
    }


