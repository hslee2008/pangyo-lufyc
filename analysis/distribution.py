def calculate_person_statistics(data, person_id):
    # Initialize values to store results for the specific person
    total_accumulation = 0
    rating_count = 0
    rating_distribution = {}

    # Loop through each category and its ratings
    for category, ratings in data.items():
        # Check if the person exists in the current category
        if person_id in ratings:
            rating = ratings[person_id]
            # Accumulate total rating for the person
            total_accumulation += rating

            # Increment the count of ratings
            rating_count += 1

            # Update the rating distribution
            if rating not in rating_distribution:
                rating_distribution[rating] = 0
            rating_distribution[rating] += 1

    return {
        "total_accumulation": total_accumulation,
        "rating_count": rating_count,
        "rating_distribution": rating_distribution
    }
def combine_distributions(data, person_ids):
    combined_distribution = {}

    # Loop through each category
    for category, ratings in data.items():
        # Loop through each person in the provided list
        for person_id in person_ids:
            if person_id in ratings:
                rating = ratings[person_id]
                # Update the combined distribution
                if rating not in combined_distribution:
                    combined_distribution[rating] = 0
                combined_distribution[rating] += 1

    return combined_distribution
def calculate_club_distributions(club, scores_data):
    club_distributions = {}

    # Loop through each club
    for club_name, members in club.items():
        # Calculate the combined distribution for the current club
        distribution = combine_distributions(scores_data, members)
        # Store the result
        club_distributions[club_name] = distribution

    return club_distributions
def get_club_distribution_for_score(club_score_distributions, score):
    result = {}
    for club_name, distribution in club_score_distributions.items():
        if score in distribution:
            result[club_name] = distribution[score]
        else:
            result[club_name] = 0  # Club has no members with that score
    return result

